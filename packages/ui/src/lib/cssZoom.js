/** Эффективный накопленный CSS zoom элемента.
 *
 *  Настройка «Масштаб интерфейса» (prefs.uiScale) вешает `zoom` на корень
 *  приложения, а Chromium считает координаты в ДВУХ разных системах:
 *  getBoundingClientRect()/clientX приходят в ЭКРАННЫХ пикселях, но left/top/
 *  transform у элемента ВНУТРИ зумленного поддерева применяются в зум-единицах
 *  (движок умножает их на zoom при отрисовке). Любой fixed-попап или transform,
 *  позиционируемый экранными координатами, обязан поделить их на этот фактор —
 *  иначе при uiScale > 100% всё уезжает вправо-вниз (жалоба владельца
 *  2026-07-17: контекст-меню и выпадашки «очень сильно смещены»).
 *
 *  currentCSSZoom — Chromium 126+ (WebView2 всегда свежее); нет свойства — 1. */
export function cssZoom(el) {
  const z = el ? el.currentCSSZoom : undefined;
  return typeof z === "number" && z > 0 ? z : 1;
}
