/**
 * full-access-demo — минимальный пример плагина Muza уровня 2 («Полный
 * доступ», право app:full-access).
 *
 * ⚠️ ПРОЧТИ ПЕРЕД ТЕМ, КАК КОПИРОВАТЬ: этот код исполняется В ОКНЕ САМОГО
 * ПРИЛОЖЕНИЯ (Rust WebviewWindow::eval), БЕЗ песочницы. Он видит весь DOM,
 * весь React хоста через DOM, window.__TAURI__ (IPC в пределах capabilities
 * Muza) — то есть буквально всё, включая сессию пользователя. Именно поэтому
 * уровень 2 в маркетплейсе идёт с премодерацией и громким экраном согласия
 * (см. PLUGINS.md §8) — устанавливай/публикуй такой код, только когда
 * ДЕЙСТВИТЕЛЬНО нельзя обойтись структурным API уровня 1 (PLUGINS.md §6).
 *
 * Этот пример НАРОЧНО делает как можно меньше — он не показывает «сколько
 * всего можно сломать», а показывает ответственный минимализм:
 *   1. Не читает и не пишет ничего чужого (ни localStorage, ни сеть, ни
 *      чужой стейт) — вообще не трогает данные пользователя.
 *   2. Всё, что делает, — ОБРАТИМО и ЗАМЕТНО: один маленький, подписанный,
 *      некликабельный (pointer-events: none) индикатор в углу — его легко
 *      узнать и не спутать с частью самого приложения.
 *   3. Не патчит прототипы, не перехватывает глобальные события/фетчи —
 *      это непредсказуемо ломает остальное приложение и другие
 *      full-access-плагины, установленные рядом.
 *   4. Асинхронный код (если он тебе нужен) заворачивай в try/catch и зови
 *      __muzaPlugin.reportError(message) сам — внешняя обёртка хоста ловит
 *      только СИНХРОННЫЙ throw во время начальной загрузки, не ошибки
 *      из промисов/таймеров.
 *
 * `__muzaPlugin` (id/name/version/reportError) — доступен здесь по замыканию,
 * его подставляет хост перед исполнением этого файла (см. PLUGINS.md §8).
 */
(function () {
  var MARK = "data-muza-plugin";

  // Идемпотентность на всякий случай: если хост когда-нибудь перезапустит
  // этот код в уже живом окне, не плодим второй индикатор.
  if (document.querySelector("[" + MARK + '="' + __muzaPlugin.id + '"]')) return;

  var badge = document.createElement("div");
  badge.setAttribute(MARK, __muzaPlugin.id);
  badge.textContent = "full-access-demo активен";
  badge.title = __muzaPlugin.name + " v" + __muzaPlugin.version + " — уровень 2 (полный доступ)";
  badge.style.cssText = [
    "position:fixed",
    "left:8px",
    "bottom:8px",
    "z-index:999999",
    "font:11px sans-serif",
    "color:#fff",
    "background:rgba(0,0,0,.55)",
    "padding:3px 8px",
    "border-radius:6px",
    "pointer-events:none",
    "opacity:.7",
  ].join(";");

  document.body.appendChild(badge);
})();
