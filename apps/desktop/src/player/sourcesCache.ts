/** In-memory кэш ответа getTrackSources по track id (слой плеера).
 *
 *  Зачем: resolveForTrack (usePlayback) ходил на сервер за источниками перед
 *  КАЖДЫМ резолвом — повторный клик по треку, возврат prev/next, круг
 *  repeat-all платили полный RTT до api.muza.lol, даже когда сам файл давно
 *  лежит в Rust LRU-кэше добычи и заиграл бы мгновенно.
 *
 *  Почему TTL в минутах безопасен: источники СУЩЕСТВУЮЩЕГО трека меняются на
 *  сервере редко (импорт дописывает новые, is_dead ставят офлайн-скрипты —
 *  см. muza-server resolver.service / mark-drm-sources), и оба по-настоящему
 *  опасных случая закрыты не TTL'ом, а явной инвалидацией:
 *   - лестница целиком не заиграла (источники протухли) → resolveForTrack
 *     сбрасывает запись при ошибке резолва, следующая попытка берёт свежие;
 *   - пользователь сменил версию трека (VersionsDialog, единственное
 *     клиентское место, меняющее источники существующего трека) → диалог
 *     инвалидирует запись рядом с cacheRemove — по той же причине, по которой
 *     он выбивает файл из кэша добычи.
 *  Устаревание «новый источник после импорта не виден до N минут» — косметика:
 *  трек, который уже играл, играет и без него.
 *
 *  Модульный singleton, а НЕ useRef в хуке: инвалидация нужна из shell-слоя
 *  (VersionsDialog), и тянуть метод хука пропсами App → диалог — лишняя
 *  связность. Плеер в окне один (usePlayback монтируется только в App).
 *  Без localStorage намеренно: устаревшие источники не должны переживать
 *  перезапуск приложения.
 *
 *  TTL абсолютный, без продления на чтении: sliding-вариант под repeat-one
 *  держал бы одну и ту же запись живой вечно. In-flight дедупликации нет
 *  сознательно — гонка «два резолва одного трека до первого ответа» узкая, а
 *  тяжёлую часть (yt-dlp) и так single-flight'ит Rust. */

import type { TrackSource } from "@muza/api-client";

/** Порядок — минуты: покрывает всплеск повторных резолвов одной сессии
 *  прослушивания (пауза на добыче → повторный клик, A↔B, короткая очередь на
 *  повторе), не отодвигая надолго горизонт «увидеть новые источники».
 *  Дубль-константа в usePlayback.test.ts — регресс-страховка на смену TTL. */
export const SOURCES_TTL_MS = 5 * 60_000;

const cache = new Map<string, { sources: TrackSource[]; at: number }>();

/** Свежая запись или null (нет/истекла — истёкшая сразу выбрасывается). */
export function getCachedSources(trackId: string): TrackSource[] | null {
  const e = cache.get(trackId);
  if (!e) return null;
  if (Date.now() - e.at > SOURCES_TTL_MS) {
    cache.delete(trackId);
    return null;
  }
  return e.sources;
}

/** Кэшируется СЫРОЙ ответ сервера: клиентская политика (applySourcePolicy)
 *  применяется на каждом резолве заново — смена настроек источников действует
 *  сразу, не дожидаясь истечения записи. */
export function putCachedSources(trackId: string, sources: TrackSource[]): void {
  cache.set(trackId, { sources, at: Date.now() });
}

/** Сбросить запись: резолв с этими источниками провалился ИЛИ пользователь
 *  сменил выбор версии (VersionsDialog) — следующий резолв идёт за свежими. */
export function invalidateCachedSources(trackId: string): void {
  cache.delete(trackId);
}
