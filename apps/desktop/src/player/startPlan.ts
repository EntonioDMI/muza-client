/** Планирование СТАРТА трека относительно того, что звучит прямо сейчас.
 *
 *  Парный модуль к gaplessPlan.ts: тот решает АВТО-стык на конце трека
 *  (кроссфейд/gapless), этот — что делать с уже играющим треком, когда старт
 *  нового требует ОЖИДАНИЯ. Ожидание тут не теоретическое: добыча каталожного
 *  трека — это поход на сервер за источниками плюс yt-dlp, который качает файл
 *  (секунды на cache-miss), см. usePlayback.resolveForTrack и src-tauri/src/engine.rs.
 *
 *  Вынесено в чистую функцию без DOM/движка — юнит-тест без мока Audio API
 *  (startPlan.test.ts), тем же приёмом, что gaplessPlan.ts. */

export interface SilenceInput {
  /** Старт пришёл от авто-перехода (естественный конец трека, ранний стык,
   *  радио-продолжение), а не от человека (клик по треку, next/prev, play,
   *  удаление играющего из очереди). */
  auto: boolean;
  /** URL нового трека уже в руках (преднагрузка) — ждать нечего. */
  preloaded: boolean;
}

/** Глушить ли то, что играет, ПЕРЕД ожиданием добычи нового трека?
 *
 *  Да — только на РУЧНОМ переключении с реальным ожиданием. Жалоба владельца
 *  (2026-07-15): «включаешь песню, она ещё не загрузилась — играет старая, и её
 *  никак не выключить, пока текущая не загрузится». Человек нажал — звук обязан
 *  ответить сразу, а не через несколько секунд, когда добыча наконец дойдёт до
 *  engine().play() и подменит источник собой.
 *
 *  Нет — на АВТО-переходе. Там старый трек либо доигрывает в кроссфейд/gapless
 *  (заглушить = убить фичу: перетекать будет нечему), либо уже кончился сам
 *  (глушить нечего), либо это радио-продолжение на конце очереди — там тишина
 *  посреди музыки была бы сюрпризом, которого никто не просил.
 *
 *  Нет — на преднагруженном. Резолва не будет вовсе, engine().play() идёт тем
 *  же тиком; пауза тут только вставила бы дырку в честный gapless.
 *
 *  Почему признак — auto, а НЕ opts.fadeSec (первый напрашивающийся вариант):
 *  fadeSec=0 бывает и у авто-перехода (кроссфейд и gapless оба выключены, а
 *  также радио-продолжение — ему нечего фейдить, следующий трек заранее
 *  неизвестен). Глушить по fadeSec===0 значило бы резать хвост трека на
 *  радио-стыке, где старый ещё звучит. usePlayback.advance() знает `auto`
 *  честно — оно и передаётся. */
export function shouldSilenceBeforeResolve(input: SilenceInput): boolean {
  return !input.auto && !input.preloaded;
}
