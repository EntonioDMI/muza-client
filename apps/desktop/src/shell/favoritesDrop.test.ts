/** Перенос трека на «Любимое» (жалоба владельца 2026-07-20: «DnD не работает
 *  для любимых»). Корень: у закреплённой строки сайдбара приёмной зоны не было
 *  вовсе — решение 16.07 «трек кладут кнопкой-сердцем». Владелец не согласен:
 *  жест естественный, раз на обычные плейлисты трек бросается.
 *
 *  Главная ловушка переноса — НЕ переиспользовать toggleLike вслепую: бросок
 *  уже любимого трека снял бы лайк, и трек исчез бы из «Любимого» ровно тем
 *  жестом, которым его туда кладут. Отсюда отдельное решение add-only. */

import { describe, expect, it } from "vitest";
import { favoritesDropAction } from "./favoritesDrop";

describe("favoritesDropAction — бросок на «Любимое» только добавляет", () => {
  it("трека нет в любимом — добавляем", () => {
    expect(favoritesDropAction("t-1", ["t-2"])).toBe("add");
  });

  it("трек УЖЕ в любимом — ничего не делаем (а НЕ снимаем лайк)", () => {
    expect(favoritesDropAction("t-1", ["t-1", "t-2"])).toBe("already");
  });

  it("пустое «Любимое» — добавляем", () => {
    expect(favoritesDropAction("t-1", [])).toBe("add");
  });
});
