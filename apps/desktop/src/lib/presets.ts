/** Матчинг пресетов настроек (спека 19.07 §4.1: «Спокойно/Живо/Ярко» + «Настроить»).
 *
 *  Инвариант: активный пресет НЕ хранится отдельным полем Prefs — в Prefs живут
 *  только конечные значения, а «какой пресет включён» ВЫЧИСЛЯЕТСЯ сравнением
 *  с наборами ниже. Иначе появляется вторая правда и рассинхрон: тронул
 *  ползунок — поле пресета осталось старым.
 *
 *  Наборы пресетов конкретных зон (PRESETS_BG и т.д.) добавляются волнами 3а-3д
 *  рядом с этой функцией, чтобы у матчинга и данных было одно место. */
import type { Prefs } from "../types";

/** Имя первого пресета, у которого совпали ВСЕ его ключи, иначе "custom".
 *  Порядок объекта = приоритет. Сравнение через JSON.stringify: значения
 *  Prefs — примитивы и маленькие плоские объекты (rowShow), для них этого
 *  достаточно и это дешевле глубокого сравнения руками. */
export function matchPreset(presets: Record<string, Partial<Prefs>>, prefs: Prefs): string {
  for (const [name, patch] of Object.entries(presets)) {
    const fits = Object.entries(patch).every(
      ([k, v]) => JSON.stringify(prefs[k as keyof Prefs]) === JSON.stringify(v),
    );
    if (fits) return name;
  }
  return "custom";
}
