/** Хост API для дев-подписи в диалогах ввода кода: коды (инвайт плейлиста, jam)
 *  живут в базе КОНКРЕТНОГО сервера и между локалхостом и продом не ходят.
 *  В проде — null: обычному пользователю хост не нужен, он всегда один.
 *
 *  `isDev` параметром, а не через `import.meta.env`, чтобы функция осталась
 *  чистой и тестируемой (и не тянула vite-типы туда, где их нет). */
export function devApiHost(baseUrl: string, isDev: boolean): string | null {
  if (!isDev) return null;
  try {
    return new URL(baseUrl).host || null;
  } catch {
    // подпись — диагностика, а не функциональность: молча исчезает, но не роняет диалог
    return null;
  }
}
