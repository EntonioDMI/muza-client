/** Детект публичного кода плейлиста в строке поиска (2026-07-17).
 *
 *  Валидный код = ВЕСЬ запрос целиком («скинь PL_…» — обычный поиск).
 *  Разделитель гибкий (_ - пробел) — коды диктуют голосом и перепечатывают
 *  руками; хвост 6–12 символов, чтобы ловить и опечатки (строгая проверка —
 *  на сервере, там же деликатный 404). Возврат — нормализованный PL_XXXXXXXX. */
export function parsePlaylistCode(q: string): string | null {
  const m = q.trim().match(/^PL[_\-\s]([A-Za-z0-9]{6,12})$/i);
  return m ? `PL_${m[1].toUpperCase()}` : null;
}

/** Детект @адреса плейлиста (2026-07-17): весь запрос целиком = @имя
 *  ([a-z0-9_]{3,32}). Возврат нормализован БЕЗ @, в нижнем регистре;
 *  «@» внутри фразы — обычный текстовый поиск. */
export function parsePlaylistHandle(q: string): string | null {
  const m = q.trim().match(/^@([A-Za-z0-9_]{3,32})$/);
  return m ? m[1].toLowerCase() : null;
}
