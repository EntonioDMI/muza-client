/** Форма див-баров статистики («Активность», «Ритм дня»): чистая геометрия
 *  «значения → высоты», отдельно от рендера — юнит-тестируется без DOM.
 *
 *  Корень бага «сплошная плашка» (2026-07-16): высоты всегда были честными
 *  (v/max), а вот ШИРИНА бара ничем не ограничивалась — flex:1 растягивал
 *  каждое ведро на всю панель. Молодая история на периоде «Всё» даёт ОДНО
 *  месячное ведро → один бар во всю ширину И высоту (сплошная плита), неделя —
 *  семь плит по ~112px. Кэп ширины (BAR_MAX_WIDTH в рендере Bars) возвращает
 *  графику форму стройных колонок при любом числе вёдер. */

/** Максимальная ширина бара, px. Держит короткую серию (1 ведро «Всё время»,
 *  7 недельных) стройными колонками; месяц (30) и часы (24) упираются в неё
 *  лишь слегка — их естественная ширина и так близка. */
export const BAR_MAX_WIDTH = 28;

/** Высота бара в процентах контейнера (пол 4% — единичные значения видны)
 *  или null для нулевого ведра — оно рендерится 2px-штрихом подложки, чтобы
 *  ряд читался как ось. Деления на ноль нет: максимум прижат к 1 снизу. */
export function barSpecs(values: number[]): (number | null)[] {
  const max = Math.max(...values, 1);
  return values.map((v) => (v > 0 ? Math.max((v / max) * 100, 4) : null));
}
